package com.example.tasc3;

public class Model1 {
    String emp,de,sa,pl,cmn,em,mo;

    public Model1() {
    }

    public Model1(String emp, String de, String sa, String pl, String cmn, String em, String mo) {
        this.emp = emp;
        this.de = de;
        this.sa = sa;
        this.pl = pl;
        this.cmn = cmn;
        this.em = em;
        this.mo = mo;
    }

    public String getEmp() {
        return emp;
    }

    public void setEmp(String emp) {
        this.emp = emp;
    }

    public String getDe() {
        return de;
    }

    public void setDe(String de) {
        this.de = de;
    }

    public String getSa() {
        return sa;
    }

    public void setSa(String sa) {
        this.sa = sa;
    }

    public String getPl() {
        return pl;
    }

    public void setPl(String pl) {
        this.pl = pl;
    }

    public String getCmn() {
        return cmn;
    }

    public void setCmn(String cmn) {
        this.cmn = cmn;
    }

    public String getEm() {
        return em;
    }

    public void setEm(String em) {
        this.em = em;
    }

    public String getMo() {
        return mo;
    }

    public void setMo(String mo) {
        this.mo = mo;
    }
}
